package histogram

import (
	"os"
)

func ExampleHist() {
	data := []float64{
		0.1,
		0.2, 0.21, 0.22, 0.22,
		0.3,
		0.4,
		0.5, 0.51, 0.52, 0.53, 0.54, 0.55, 0.56, 0.57, 0.58,
		0.6,
		// 0.7 is empty
		0.8,
		0.9,
		1.0,
	}

	hist := Hist(9, data)

	if err := FPrint(os.Stdout, hist, Linear(5)); err != nil {
		panic(err)
	}
	// Output:
	// 0.1-0.2  5%   ▉       [1/20]
	// 0.2-0.3  25%  ██▉     [5/20]
	// 0.3-0.4  0%   ▏       [0/20]
	// 0.4-0.5  5%   ▉       [1/20]
	// 0.5-0.6  50%  █████▏  [10/20]
	// 0.6-0.7  0%   ▏       [0/20]
	// 0.7-0.8  0%   ▏       [0/20]
	// 0.8-0.9  5%   ▉       [1/20]
	// 0.9-1    10%  █▏      [2/20]
}
